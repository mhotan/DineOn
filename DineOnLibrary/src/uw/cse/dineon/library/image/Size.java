package uw.cse.dineon.library.image;

/**
 * Stupid helper class because I couldn't find a convenient one.
 * @author mhotan
 */
public class Size {

	public final int width;
	public final int height;
	
	/**
	 * Creates a size instance to use in general image situations.
	 * @param w width
	 * @param h height
	 */
	public Size(int w, int h) {
		width = w;
		height = h;
	}
	
}
